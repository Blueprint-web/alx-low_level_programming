#include "3-calc.h"
/**
 * op_add - the addition function
 * @a: the first value
 * @b: the second value
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - the subtraction function
 * @a: first value
 * @b: the second value
 * Return: the sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - the multiplication function
 * @a: the first value
 * @b: the second value
 * Return: the sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - the divid function
 * @a: the first value
 * @b: the second value
 * Return: the sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the modulus function
 * @a: the first value
 * @b: the second value
 * Return: the sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
